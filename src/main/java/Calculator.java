public class Calculator {
    public int Add(int x, int y){
        int result = x + y;
        return result;
    }

    public int Subtract(int x, int y) {
        int result = x - y;
        return result;
    }

    public int Multiply(int x, int y) {
        int result = x * y;
        return result;
    }

    public double Divide(double x, double y){
        double result = x / y;
        return result;
    }
}

